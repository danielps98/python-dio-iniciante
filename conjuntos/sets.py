# um set uma colecao que não aceita repetição
# usamos sets para reprentar um conjunto
# matematicos ou eliminar item duplicados  de um iteravel

# exemplo
set ([1, 2, 3, 4, 5 , 1 ,4 ,2 ,3, 4, 5]) # {1, 2, 3, 4, 5}
# nao repete os numero ele faz meio que um filto 

set ("abacaxi") # {'a', 'b', 'c', 'i', 'x'}

set (("palio","gol", "celta", "palio" )) # {'palio', 'gol', 'celta'}

