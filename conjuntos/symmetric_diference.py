conjunto_a = {1, 2, 3}
conjunto_b = {2, 3, 4}

conjunto_a.symmetric_difference(conjunto_b) # {1, 4}
# A diferença simétrica de conjuntos é uma operação que retorna os elementos que estão presentes em um dos conjuntos, mas não em ambos. Ou seja, ela retorna os elementos que estão em A ou B, mas não em A e B ao mesmo tempo.
